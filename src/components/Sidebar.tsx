function getNavLinks(role: Role, basePath: string): NavItem[] {
  let links: NavItem[];
  switch (role) {
    case "admin":
      links = adminLinks;
      break;
    case "student_affairs":
      links = studentAffairsLinks;
      break;
    case "student":
      links = studentLinks;
      break;
  }
  return links.map((link) => ({ ...link, href: `${basePath}${link.href}` }));
}

function getRoleLabel(role: Role): string {
  switch (role) {
    case "admin":
      return "Admin";
    case "student_affairs":
      return "Student Affairs";
    case "student":
      return "Student";
  }
}

